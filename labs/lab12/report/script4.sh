#!/bin/bash

# Проверка количества аргументов
if [ $# -ne 2 ]; then
    echo "Использование: $0 <расширение> <путь_к_директории>"
    echo "Пример: $0 .txt /home/user/docs"
    exit 1
fi

EXT="$1"
DIR="$2"

# Убираем точку в начале расширения, если она есть
EXT="${EXT#.}"

# Проверяем директорию
if [ ! -d "$DIR" ]; then
    echo "Ошибка: $DIR не является каталогом"
    exit 1
fi

# Подсчёт файлов (не рекурсивно)
count=$(find "$DIR" -maxdepth 1 -type f -name "*.$EXT" | wc -l)

echo "В каталоге $DIR найдено $count файлов с расширением .$EXT"

